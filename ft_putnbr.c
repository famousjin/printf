#include "libftprintf.h"

void	ft_putnbr(int n, int *i)
{
	char	num;

	if (n == -2147483648)
		write(1, "-2147483648", 11);
	if (n < 0)
	{
		n *= -1;
		*i = *i + write(1, "-", 1);
	}
	if (n <= 9)
	{
		num = '0' + n;
		*i = *i + write(1, &num, 1);
	}
	else
	{
		ft_putnbr(n / 10, i);
		ft_putnbr(n % 10, i);
	}
}
